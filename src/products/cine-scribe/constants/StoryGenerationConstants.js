export const normalizeText = (text) => text.replace(/\s+/g, " ").trim();

export const validateField = (name, value) => {
  const field = name.toLowerCase();


  // Skip empty or undefined values
  if (!value || (Array.isArray(value) && value.length === 0)) return null;

  const isDropdown = [
    "genres",
    "themes",
    "targetaudience",
    "endingtype",
    "narrationstyle",
  ].includes(field);

  if (isDropdown) {
    if (value.length > wordLimits[field]) {
      return `Select at most ${wordLimits[field]} ${field}`;
    }
  } else {
    const wordCount =
      typeof value === "string" ? value.trim().split(/\s+/).length : 0;

    if (minWordLimits[field] && wordCount < minWordLimits[field]) {
      return `${wordDisplay[field]} must be at least ${minWordLimits[field]} words`;
    }

    if (wordCount > wordLimits[field]) {
      return `${wordDisplay[field]} must be at most ${wordLimits[field]} words`;
    }
  }

  return null;
};

export const validateForm = (formData) => {
 
  const errors = {};

  // Required fields
  if (!formData.story_title?.trim()) {
    // errors.title = `Title should be atleast ${minWordLimits.title} word`;
    errors.title =`Title is required`;
  }
  if (!formData.backdrop?.trim()) {
    // errors.backdrop = `Backdrop should be atleast ${minWordLimits.backdrop} word`;
    errors.backdrop = `Backdrop is required`;

  }
  if (!formData.user_genres || formData.user_genres.length === 0) {
    errors.genres = "Please select at least one genre";
  }
  if (!formData.themes || formData.themes.length === 0) {
    errors.themes = "Please select at least one theme";
  }
  if (!formData.logline?.trim()) {
    errors.logline = `Logline is required`;
  }
  if (!formData.key_plot_points?.trim()) {
    errors.keyplotpoints = `Key Plot Points is required`;
  }

  // Validate core fields
  const coreFields = {
    title: formData.story_title,
    backdrop: formData.backdrop,
    genres: formData.user_genres,
    themes: formData.themes,
    logline: formData.logline,
    keyplotpoints: formData.key_plot_points,
  };

  Object.entries(coreFields).forEach(([name, value]) => {
    const error = validateField(name, value);
    if (error) errors[name.toLowerCase()] = error;
  });

  // Validate advanced features (optional fields)
  fields.forEach((field) => {
    const value = formData.advancedFeatures?.[field];
    if (!value || (Array.isArray(value) && value.length === 0)) return;

    const error = validateField(field, value);
    if (error) {
      const key = field.replace(/\s+/g, "").toLowerCase();
      errors[key] = error;
    }
  });
  return errors;
};




